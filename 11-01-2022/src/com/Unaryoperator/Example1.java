package com.Unaryoperator;

public class Example1 {

	public static void main(String[] args) 
	{
        //increment operation
		
		
		int i=5;
		System.out.println(i++);
		System.out.println("postincrement  : "+i);   //post increment
		
		int j=3;
		System.out.println(++j);			//preincrement
		System.out.println("preincrement  :   "+j);
		
		
		System.out.println("*************************");
		
		
		//decrement operation
		
		int a=3;
		System.out.println(a--);
		System.out.println("postdecrement  : "+a);   //post increment
		int b=2;
		System.out.println(--b);			//preincrement
		System.out.println("predecrement  :   "+b);
		
	}

}
